#!/usr/bin/env node
import { readFileSync, readdirSync, statSync, mkdirSync, writeFileSync } from 'fs';
import { join, relative } from 'path';
import yaml from 'js-yaml';

const ROOT = new URL('..', import.meta.url).pathname.replace(/\/$/, '');
const SCHEMA = join(ROOT, 'schema');

function walk(dir, ext = '.yaml') {
  const results = [];
  for (const f of readdirSync(dir)) {
    const p = join(dir, f);
    if (statSync(p).isDirectory()) results.push(...walk(p, ext));
    else if (f.endsWith(ext)) results.push(p);
  }
  return results;
}

const edges = [];
const files = walk(SCHEMA);

for (const fp of files) {
  const rel = relative(ROOT, fp);
  const doc = yaml.load(readFileSync(fp, 'utf8'));
  if (!doc || !doc.name) continue;

  if (rel.includes('/functions/')) {
    const src = `fn:${doc.name}`;
    if (Array.isArray(doc.steps)) {
      for (const step of doc.steps) {
        if (step.call) edges.push([src, `fn:${step.call}`]);
      }
    }
    if (Array.isArray(doc.inputs)) {
      for (const input of doc.inputs) {
        if (input.type && /^[A-Z]/.test(input.type)) {
          const base = input.type.replace(/\[\]$/, '');
          edges.push([src, `entity:${base}`]);
        }
      }
    }
  }

  if (rel.includes('/commands/')) {
    const src = `cmd:${doc.name}`;
    if (doc.action?.call) edges.push([src, `fn:${doc.action.call}`]);
    if (Array.isArray(doc.steps)) {
      for (const step of doc.steps) {
        if (step.call) edges.push([src, `fn:${step.call}`]);
      }
    }
  }

  if (rel.includes('/routes/')) {
    const src = `route:${doc.name}`;
    if (doc.entity) edges.push([src, `entity:${doc.entity}`]);
    if (Array.isArray(doc.operations)) {
      for (const op of doc.operations) {
        if (op.call) edges.push([src, `fn:${op.call}`]);
      }
    }
  }
}

// Deduplicate
const unique = [...new Set(edges.map(([a, b]) => `  ${a} --> ${b}`))].sort();

const mermaid = `\`\`\`mermaid\ngraph LR\n${unique.join('\n')}\n\`\`\``;
const output = `# Dependency Graph\n\nAuto-generated by \`scripts/generate-dep-graph.mjs\`.\n\n${mermaid}\n`;

mkdirSync(join(ROOT, 'docs'), { recursive: true });
writeFileSync(join(ROOT, 'docs', 'dependency-graph.md'), output);
console.log(`✅ Wrote docs/dependency-graph.md (${unique.length} edges)`);
